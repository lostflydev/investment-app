package com.example.investment.concurrency;

/**
 * Упрощённый «торговый стакан» — ограниченный пул акций на продажу.
 *
 * Доступно ровно {@link #available()} штук. Каждый клиент пытается купить
 * {@code qty} штук. Если хватает — сделка совершается, счётчик уменьшается.
 *
 * Идея учебного материала: показать студентам, что «простая» операция
 *
 * <pre>
 *   if (available &gt;= qty) available -= qty;
 * </pre>
 *
 * ломается в многопоточном окружении (race condition: два потока одновременно
 * проходят проверку, оба вычитают). Разные реализации демонстрируют разные
 * подходы к решению.
 */
public interface OrderBook {

    /**
     * Попытаться купить {@code qty} акций от имени клиента {@code clientId}.
     * @return {@code true} если сделка прошла, {@code false} если не хватило акций.
     */
    boolean buy(int qty, String clientId);

    /** Сколько акций ещё доступно для покупки. Может быть отрицательным в Unsafe-версии. */
    int available();

    /** Сколько всего продано. */
    int sold();

    /**
     * Сколько акций продано сверх начального пула.
     * В корректной реализации должно быть всегда 0.
     * В {@link UnsafeOrderBook} — больше нуля при race condition.
     */
    int oversold();

    /** Сбросить состояние. Пул становится равным {@code initial}, продано — 0. */
    void reset(int initial);

    /** Человекочитаемое имя реализации для UI и логов. */
    String name();

    /** Журнал событий — для отображения в UI. */
    TradeLog log();
}
